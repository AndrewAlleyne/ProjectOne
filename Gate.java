
public class Gate {

    private int playerSpace;
    private int castleGates;

    int AgateSpace = 3;
    int DgateSpace = 3;

    public Gate(int playerSpace, int castleGates) {
        this.playerSpace = playerSpace;
        this.castleGates = castleGates;

    }

    public int gateSpace(String name) {

        return AgateSpace--;

    }

    public int gateSpace2(String name) {

        return DgateSpace--;

    }

}
